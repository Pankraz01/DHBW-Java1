package dhbw.java1.aLotOfTests;
public class BinaryArithmeticOperators {
	
    public static void main(String[] args) {
        int i = 17, j = 3;
        System.out.println(i + j);          // Addition
        System.out.println(i - j);          // Subtraktion
        System.out.println(i * j);          // Multiplikation
        System.out.println(i / j);          // Division
        System.out.println(i % j);          // Modulo
        System.out.println(0.1 + 0.2);		//Ungenauigkeiten in der Rechnung durch Bin√§rsystem
    }
}
